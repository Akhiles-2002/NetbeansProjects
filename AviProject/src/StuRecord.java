    private void addrecActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addrecActionPerformed
        try{
        Class.forName("com.mysql.jdbc.Driver");
       Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/avidb","root","india123");
       String query="insert into sturec(name,college,gender,language,stream,images)values(?,?,?,?,?,?) ";
       PreparedStatement pst=con.prepareStatement(query);
       pst.setString(1,nametxt.getText());
        pst.setString(2,collegetxt.getText());
        if(malebtn.isSelected())
        {
            gender="Male";
        }
        if(femalebtn.isSelected())
        {
            gender="Female";
        }
        pst.setString(3,gender);
        
        String language="";
        if(javabtn.isSelected())
        {
            language=javabtn.getText()+" ";
        }
         if(pythonbtn.isSelected())
        {
            language=pythonbtn.getText()+" ";
        }
        pst.setString(4,language);
        String course;
        course=streambtn.getSelectedItem().toString();
        pst.setString(5,course);
        pst.setBytes(6,person_image);
        
        pst.executeUpdate();
        JOptionPane.showMessageDialog(null,"Inserted successfully!!!");
        }
    
         catch(Exception e)
       {
           JOptionPane.showMessageDialog(null,e);
       }
    }//GEN-LAST:event_addrecActionPerformed


    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
       int i=jTable1.getSelectedRow();
       TableModel model=jTable1.getModel();
       nametxt.setText(model.getValueAt(i,0).toString());
       collegetxt.setText(model.getValueAt(i,1).toString());
       String gender=model.getValueAt(i,2).toString();
         if(gender.equals("Male"))
         {
             malebtn.setSelected(true);
         }
         else
         {
             femalebtn.setSelected(true); 
         }
         
       String language=model.getValueAt(i,3).toString();
       switch(language)
       {
           case "Java ":
           javabtn.setSelected(true);
           pythonbtn.setSelected(false);
           break;
           case "Python ":
           pythonbtn.setSelected(true);
           javabtn.setSelected(false);
           break;
           default:
            javabtn.setSelected(true);   
            pythonbtn.setSelected(true);
           break;
       }
       String stream1=model.getValueAt(i,4).toString();
       switch(stream1)
       {
           case "Computer Science":
               streambtn.setSelectedIndex(0);
               break;
           case "Management":
               streambtn.setSelectedIndex(1);
               break;    
            case "Bio Technology":
               streambtn.setSelectedIndex(2);
               break;   
            case "Medical Science":
               streambtn.setSelectedIndex(3);
               break;  
               
       }
       byte[]img=(userList().get(i).getImage());
       ImageIcon imgicon=new ImageIcon(new ImageIcon(img).getImage().getScaledInstance(photo.getWidth(),photo.getHeight(),Image.SCALE_SMOOTH));
      photo.setIcon(imgicon);
        
    }//GEN-LAST:event_jTable1MouseClicked

    private void photobtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_photobtnActionPerformed
        // TODO add your handling code here:
      JFileChooser chooser=new JFileChooser();  
      chooser.showOpenDialog(null);
      File f=chooser.getSelectedFile();
      filename=f.getAbsolutePath();
      ImageIcon imgicon=new ImageIcon(new ImageIcon(filename).getImage().getScaledInstance(photo.getWidth(),photo.getHeight(),Image.SCALE_SMOOTH));
      photo.setIcon(imgicon);
      try
      {
       File image=new File(filename);
       FileInputStream fis=new FileInputStream(image);
       ByteArrayOutputStream bos=new ByteArrayOutputStream();
       byte[] buf=new byte[1024];
       for(int readNum;(readNum=fis.read(buf))!=-1;)
       {
           bos.write(buf,0,readNum);
       }
       person_image=bos.toByteArray();
      }
      catch(Exception e)
      {
          JOptionPane.showMessageDialog(null,e);
      }
      
      
    }//GEN-LAST:event_photobtnActionPerformed


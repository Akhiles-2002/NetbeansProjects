    public ArrayList<User> userList()
    {
        ArrayList userList=new ArrayList<>();
        try{
        Class.forName("com.mysql.jdbc.Driver");
       Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/avidb","root","india123");
       String query1="select * from sturec";
       Statement stmt=con.createStatement();
       ResultSet rs=stmt.executeQuery(query1);
       User user;
       while(rs.next())
       {
           user=new User(rs.getString("name"),rs.getString("college"),rs.getString("gender"),rs.getString("language"),rs.getString("stream"),rs.getBytes("images"));
       userList.add(user);
       }
        }
        
          catch(Exception e)
       {
           JOptionPane.showMessageDialog(null,e);
       }
        return userList;
    }
    public void show_rec()
    {
        ArrayList<User> list= userList();
        DefaultTableModel model=(DefaultTableModel)jTable1.getModel();
        Object[]row=new Object[5];
        for(int i=0;i<list.size();i++)
        {
         row[0]=list.get(i).getname();
         row[1]=list.get(i).getcollege();
         row[2]=list.get(i).getgender();
         row[3]=list.get(i).getlanguage();
         row[4]=list.get(i).getstream();
         model.addRow(row);
         
        }
        
    }

